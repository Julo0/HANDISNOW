    12.38992131 , 2.00872523 , -2.94879393 , -13.68491049 , -11.44463939 , 10.01556392 ; ...
    -11.75684996 , 10.17151640 , -9.19058758 , 9.13105467 , -8.31173569 , 9.29292029] ;

